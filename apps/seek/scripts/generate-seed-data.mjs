function buildDescription({ title, employer, intro, responsibilities, requirements, offer }) {
  return [
    `## About the role`,
    "",
    intro,
    "",
    `## What you'll be doing`,
    "",
    ...responsibilities.map((r) => `- ${r}`),
    "",
    `## What we're looking for`,
    "",
    ...requirements.map((r) => `- ${r}`),
    "",
    `## What's on offer`,
    "",
    ...offer.map((o) => `- ${o}`),
    "",
    `## About ${employer.name}`,
    "",
    employer.about,
    "",
    `_Apply now to join ${employer.name} as a ${title}. We're an equal-opportunity employer and welcome applications from candidates of all backgrounds._`,
  ].join("\n");
}

